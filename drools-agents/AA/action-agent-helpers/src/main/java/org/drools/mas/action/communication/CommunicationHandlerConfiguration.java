package org.drools.mas.action.communication;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

/**
 *
 * @author esteban
 */
public class CommunicationHandlerConfiguration implements Serializable {
    private String sender;
    private String source;
    private String originator;
    private List<String> receivers = new ArrayList<String>();
    private List<String> subjects = new ArrayList<String>();
    private List<String> channels = new ArrayList<String>();
    private List<String> templates = new ArrayList<String>();
    private List<String> timeouts = new ArrayList<String>();
    private Map<String, Object> templateVariables = new HashMap<String, Object>();

    public String getSender() {
        return sender;
    }

    public void setSender(String sender) {
        this.sender = sender;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public String getOriginator() {
        return originator;
    }

    public void setOriginator(String originator) {
        this.originator = originator;
    }

    public List<String> getChannels() {
        return channels;
    }

    public void setChannels(List<String> channels) {
        this.channels = channels;
    }

    public List<String> getReceivers() {
        return receivers;
    }

    public void setReceivers(List<String> receivers) {
        this.receivers = receivers;
    }

    public List<String> getTemplates() {
        return templates;
    }

    public void setTemplates(List<String> templates) {
        this.templates = templates;
    }

    public List<String> getTimeouts() {
        return timeouts;
    }

    public void setTimeouts(List<String> timeouts) {
        this.timeouts = timeouts;
    }

    public Map<String, Object> getTemplateVariables() {
        return templateVariables;
    }

    public void setTemplateVariables(Map<String, Object> templateVariables) {
        this.templateVariables = templateVariables;
    }

    public List<String> getSubjects() {
        return subjects;
    }

    public void setSubjects(List<String> subjects) {
        this.subjects = subjects;
    }

}
